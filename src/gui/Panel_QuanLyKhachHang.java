    private void btnTimKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKHActionPerformed
        // TODO add your handling code here:
        String maKH = txtTim.getText();
        if (maKH.equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập mã khách hàng cần tìm", "Tìm khách hàng", JOptionPane.INFORMATION_MESSAGE);
            xoaHetDuLieuTable();
            docDuLieuTuListVaoTable(kh_dao.getAllKhachHang());
            tb_kh.clearSelection();
            txtTim.grabFocus();
        } else {
            ArrayList<KhachHang> dsKhachHangs = new ArrayList<>();
            for (KhachHang khachHang : this.dskh) {
                if (khachHang.getMaKH().toLowerCase().contains(maKH.toLowerCase())) {
                    dsKhachHangs.add(khachHang);
                }
            }
            if (!dsKhachHangs.isEmpty()) {
                xoaHetDuLieuTable();
                docDuLieuTuListVaoTable(dsKhachHangs);
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy", "Tìm khách hàng", JOptionPane.INFORMATION_MESSAGE);
                xoaHetDuLieuTable();
                docDuLieuTuListVaoTable(kh_dao.getAllKhachHang());
                tb_kh.clearSelection();    
                txtTim.grabFocus();
            }
            
        }
    }//GEN-LAST:event_btnTimKHActionPerformed

