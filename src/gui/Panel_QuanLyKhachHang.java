    public void xoaHetDuLieuTable() {
        DefaultTableModel dtm = (DefaultTableModel)tb_kh.getModel();
        dtm.getDataVector().removeAllElements();
    }
    
    public void docDuLieuTuListVaoTable(ArrayList<KhachHang> dskh) {
        for (KhachHang kh : dskh) {
            modelKH.addRow(new Object[] {
                kh.getMaKH(), kh.getTenKH(), kh.getDiaChi(), kh.getSdt(), kh.getEmail(), kh.getDiemTL()
            });
        }
    }

