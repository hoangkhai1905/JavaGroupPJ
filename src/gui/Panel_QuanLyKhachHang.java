    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String maKH = txtMaKH.getText();
        String tenKH = txtTenKH.getText();
        String diaChi = txtDiaChi.getText();
        String sdt = txtSDT.getText();
        String email = txtEmail.getText();
        int diemTL = Integer.parseInt(txtDiemTL.getText());
        KhachHang kh = new KhachHang(maKH, tenKH, diaChi, sdt, email, diemTL);
        
        if (kh_dao.create(kh)) {
            JOptionPane.showMessageDialog(this, "Tạo thành công", "Tạo khách hàng", JOptionPane.INFORMATION_MESSAGE);
            xoaHetDuLieuTable();
            docDuLieuTuListVaoTable(kh_dao.getAllKhachHang());
        }
    }//GEN-LAST:event_btnCreateActionPerformed

