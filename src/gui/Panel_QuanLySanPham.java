    public void xoaHetDuLieuTable() {
        DefaultTableModel dtm = (DefaultTableModel)tb_sp.getModel();
        dtm.getDataVector().removeAllElements();
    }
    
    public void docDuLieuTuListVaoTable(ArrayList<SanPham> dssp) {
        for (SanPham sp : dssp) {
            modelSanPham.addRow(new Object[] {
                sp.getMaSP(), sp.getTenSP(), sp.getMoTa(),sp.getDonViTinh(), String.format("%.0f", sp.getGiaNhap()), sp.getSoTon(), sp.getNgayNhap().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")), sp.getNgaySX().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")), sp.getNgayHH().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")), sp.getMaNhom(), sp.getMaNCC()
            });
        }
        tb_sp.clearSelection();
    }

