    private void btnTimSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimSPActionPerformed
        // TODO add your handling code here:
        String maSP = txtMaSP.getText();
        if (maSP == null || maSP.equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập mã sản phẩm cần tìm", "Tìm kiếm sản phẩm", JOptionPane.WARNING_MESSAGE);
        } else {
            xoaHetDuLieuTable();
            ArrayList<SanPham> dssp = new ArrayList<>();
            for (SanPham sanPham : this.dssp) {
                if (sanPham.getMaSP().toLowerCase().contains(maSP.toLowerCase())) {
                    dssp.add(sanPham);
                }
            }
            if (dssp.size()> 0) {
                for (SanPham sp : dssp) {
                    modelSanPham.addRow(new Object[] {
                        sp.getMaSP(), sp.getTenSP(), sp.getMoTa(),sp.getDonViTinh(), sp.getGiaNhap(), sp.getSoTon(), sp.getNgayNhap().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")), sp.getNgaySX().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")), sp.getNgayHH().format(DateTimeFormatter.ofPattern("dd-MM-yyyy")), sp.getMaNhom(), sp.getMaNCC()
                    });
                }
            } else {
                JOptionPane.showMessageDialog(this, "Mã sản phẩm không tồn tại", "Tìm kiếm sản phẩm", JOptionPane.INFORMATION_MESSAGE);
                xoaHetDuLieuTable();
                docDuLieuTuListVaoTable(sp_dao.getAllSanPham());
            }
        }
    }//GEN-LAST:event_btnTimSPActionPerformed

